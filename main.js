function setLanguage(lang) {
  const dict = translations[lang];
  if (!dict) return;

  document.querySelectorAll("[data-i18n-key]").forEach((el) => {
    const key = el.getAttribute("data-i18n-key");
    const val = dict[key];
    if (!val) return;
    if (key === "footer-credits") {
      el.innerHTML = val;
    } else {
      el.textContent = val;
    }
  });

  document.documentElement.lang = lang;
  document.querySelectorAll(".lang-toggle").forEach((btn) =>
    btn.classList.toggle("is-active", btn.dataset.lang === lang),
  );

  updatePlaceholders(lang);
}

function updatePlaceholders(lang) {
  const ph = {
    pt: {
      name: "Seu nome",
      email: "Seu e-mail",
      topic: "O que você procura?",
      message: "Conte brevemente o que você precisa\u2026",
    },
    en: {
      name: "Your name",
      email: "Your email",
      topic: "What are you looking for?",
      message: "Briefly share what you need\u2026",
    },
  };
  const t = ph[lang] || ph.pt;
  const form = document.querySelector(".contact-form");
  if (!form) return;
  const nameInput = form.querySelector('input[name="name"]');
  const emailInput = form.querySelector('input[name="email"]');
  const select = form.querySelector("select");
  const textarea = form.querySelector("textarea");
  if (nameInput) nameInput.placeholder = t.name;
  if (emailInput) emailInput.placeholder = t.email;
  if (select && select.options[0]) select.options[0].textContent = t.topic;
  if (textarea) textarea.placeholder = t.message;
}

function initLanguageToggle() {
  document.querySelectorAll(".lang-toggle").forEach((btn) => {
    btn.addEventListener("click", () => {
      setLanguage(btn.dataset.lang);
      initCurrentYear();
    });
  });

  const prefersEnglish =
    navigator?.language?.toLowerCase().startsWith("en");
  if (prefersEnglish) {
    setLanguage("en");
  }
}

function initCurrentYear() {
  const el = document.getElementById("current-year");
  if (el) el.textContent = String(new Date().getFullYear());
}

function initMobileMenu() {
  const toggle = document.getElementById("menu-toggle");
  const nav = document.getElementById("main-nav");
  if (!toggle || !nav) return;

  toggle.addEventListener("click", () => {
    const open = nav.classList.toggle("is-open");
    toggle.classList.toggle("is-open", open);
    toggle.setAttribute("aria-expanded", String(open));
  });

  nav.querySelectorAll("a").forEach((link) => {
    link.addEventListener("click", () => {
      nav.classList.remove("is-open");
      toggle.classList.remove("is-open");
    });
  });
}

document.addEventListener("DOMContentLoaded", () => {
  initLanguageToggle();
  initCurrentYear();
  initMobileMenu();
});
